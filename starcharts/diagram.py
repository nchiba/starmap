class Diagram:
    def __init__(self, area, star_data_list, skyculture="western", star_color="white"):
        self.cons_lines = []
        current_path = os.path.dirname(__file__)
        skyculture = f"../skycultures/{skyculture}/constellationship.fab"
        skyculture_file = os.path.normpath(os.path.join(current_path, skyculture))
        with open(skyculture_file, "r") as cons_file:
            for line in cons_file.readlines():
                line = re.sub(r"\s+", " ", line).strip()
                line_arr = line.split(" ")
                if len(line_arr) < 2 or (len(line_arr) % 2) == 1:
                    continue

                for idx in range(2, len(line_arr), 2):
                    self.cons_lines.append((line_arr[idx], line_arr[idx + 1]))

        self.area = area
        self.star_data_list = star_data_list
        self.curves = []
        self.star_color = star_color
        self.border_min_x = (
            self.border_min_y
        ) = self.border_max_x = self.border_max_y = None

    def add_curve(self, curve_points):
        self.curves.append(curve_points)

    def _mag_to_d(self, m):
        mag_range = DIMMEST_MAG - BRIGHTEST_MAG
        m_score = (DIMMEST_MAG - m) / mag_range
        r_range = MAX_D - MIN_D
        return MIN_D + m_score * r_range

    def _invert_and_offset(self, x, y):
        return x + MARGIN_X, (self.star_data_list.max_y - y) + MARGIN_Y

    def get_svg(self):
        svg = Svg()

        # coords
        center_x = self.star_data_list.max_x / 2 + MARGIN_X
        center_y = self.star_data_list.max_y / 2 + MARGIN_Y
        svg.circle2(center_x, center_y, 253, 4, self.star_color)

        # add stars first
        for star_data in self.star_data_list.data:
            x, y = self._invert_and_offset(star_data.x, star_data.y)
            svg.circle(x, y, self._mag_to_d(star_data.mag), self.star_color)

        # add lines
        for con in self.cons_lines:
            left = con[0]
            right = con[1]

            if left not in MAPPING:
                continue

            left_label = MAPPING[left]
            left_star = next(
                (star for star in self.star_data_list.data if star.label == left_label),
                None,
            )
            if not left_star:
                continue

            if right not in MAPPING:
                continue

            right_label = MAPPING[right]
            right_star = next(
                (
                    star
                    for star in self.star_data_list.data
                    if star.label == right_label
                ),
                None,
            )
            if not right_star:
                continue

            x1, y1 = self._invert_and_offset(left_star.x, left_star.y)
            x2, y2 = self._invert_and_offset(right_star.x, right_star.y)
            svg.line(x1, y1, x2, y2, 1, self.star_color)

        # next add labels
        for star_data in self.star_data_list.data:
            if star_data.label:
                x, y = self._invert_and_offset(star_data.x, star_data.y)
                self._mag_to_d(star_data.mag)

        return svg.to_list()
